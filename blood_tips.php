                                        When To Not Donate Blood</a>
                                      </h4>
                                    </div>
                                    <div id="collapse5" class="panel-collapse collapse">
                                      <div class="panel-body">You should NOT donate blood if : <br><br>-    Cold / fever in the past 1 week.
<br>-   Under treatment with antibiotics or any other medication.
<br>-   Cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer, chronic kidney or liver disease, bleeding tendencies, venereal disease etc.
<br>-   Major surgery in the last 6 months.
<br>-   Vaccination in the last 24 hours.
<br>-   Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.
<br>-   Had fainting attacks during last donation.
<br>-   Have regularly received treatment with blood products.
<br>-   Shared a needle to inject drugs/ have history of drug addiction.
<br>-   Had sexual relations with different partners or with a high risk individual.
<br>-   Been tested positive for antibodies to HIV.</div>
