                                    <div id="collapse4" class="panel-collapse collapse">
                                      <div class="panel-body"> You should donate blood if : <br><br>
-   You are between age group of 18-60 years.
<br>-   Your weight is 45 kgs or more.
<br>-   Your hemoglobin is 12.5 gm% minimum.
<br>-   Your last blood donation was 3 or more months earlier.
<br>-   You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past.</div>
