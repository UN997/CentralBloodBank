                                    <div id="collapse3" class="panel-collapse collapse">
                                      <div class="panel-body">The most common blood type is O, followed by type A.
<br><br>
Type O individuals are often called "universal donors" since their blood can be transfused into persons with any blood type. Those with type AB blood are called "universal recipients" because they can receive blood of any type.
<br><br>
However, since approximately twice as many people in the general population have O and A blood types, a blood bank's need for this type of blood increases exponentially.</div>
